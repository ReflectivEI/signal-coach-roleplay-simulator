export type EvaluationPromptInput = {
    scenarioContext: string;
    transcript: string;
    voiceSummary: string;
    selectedDropdowns: string;
    repTemperature: number;
    /** Injected from hcpBrain.js when brain selections are present. */
    hcpBrainContext?: string;
};

export function buildEvaluationPrompt(input: EvaluationPromptInput): string {
    return [
        "You are evaluating a synthetic REP/HCP interaction for coaching.",
        "",
        ...(input.hcpBrainContext ? [input.hcpBrainContext, ""] : []),
        "Hard constraints:",
        "- Evaluate REP behavior only.",
        "- Use transcript evidence and voice metadata.",
        "- No PHI, no patient-specific assumptions.",
        "- Do not inflate scores for polished but generic language.",
        "- If cue alignment is weak, cap most metrics in low-mid range.",
        "",
        "Evaluate all layers:",
        "1) Semantic analysis",
        "2) Response structure",
        "3) Delivery quality",
        "4) Cue alignment",
        "5) Strategic effectiveness",
        "6) Conversational outcome",
        "",
        "Context:",
        input.scenarioContext,
        "",
        "REP transcript:",
        input.transcript,
        "",
        "Voice summary:",
        input.voiceSummary,
        "",
        "Dropdowns:",
        input.selectedDropdowns,
        "",
        `REP selected temperature: ${input.repTemperature}`,
        "",
        "Scoring calibration (1-10):",
        "- 1-3: missed cue, generic pitch, no acknowledgment/question, increases resistance.",
        "- 4-5: partial relevance, surface acknowledgment, weak specificity, little movement.",
        "- 6-7: solid but incomplete, some cue recognition, moderate movement.",
        "- 8: strong, specific, cue-aligned, advances conversation.",
        "- 9-10: exceptional precision and adaptation, context-rich, temperature-aware progression without overforcing.",
        "",
        "Metric calibration anchors:",
        "- context_awareness: low=generic, high=natural use of journey/disease/pressure/access context.",
        "- cue_recognition: low=surface words only, high=underlying cue + hidden resistance.",
        "- empathy_acknowledgment: low=canned/none, high=practical acknowledgment without stalling.",
        "- strategic_questioning: low=none/closed, high=precise open question revealing decision criteria.",
        "- evidence_framing: low=data dump/vague, high=evidence tied to active concern and stage.",
        "- objection_handling: low=deflect/argue, high=name+narrow+respond without overclaim.",
        "- conversational_control: low=stall or premature close, high=appropriate progression for resistance.",
        "- tone_pace_confidence: low=mismatched, high=measured and aligned to pressure/temperature.",
        "",
        "Outcome calibration by temperature:",
        "- High temperature: reduced resistance or permission to continue can be strong outcomes.",
        "- Mid temperature: fit exploration, data willingness, or barrier clarification are strong outcomes.",
        "- Low temperature: expect clearer next-step movement (follow-up/data review/patient identification/workflow action).",
        "",
        "Coaching quality requirements:",
        "- Quote exact REP phrase(s) when available.",
        "- Explain how HCP likely interpreted those phrase(s).",
        "- Identify missed cue and behavioral consequence.",
        "- Provide one realistic improved alternative phrase.",
        "- Provide one next-best question.",
        "- Keep coaching conversational; avoid corporate filler.",
        "",
        "Return strict JSON with keys:",
        "overall_score, metric_scores, observed_strengths, missed_cues, delivery_issues,",
        "outcome_analysis, coaching_feedback, better_phrasing, next_best_question,",
        "what_hcp_likely_heard, improved_response_example, voice_behavior_adaptation,",
        "delivery_impact_on_hcp, delivery_coaching,",
        "hcp_brain_alignment, hcp_brain_coaching",
        "",
        "hcp_brain_alignment must include (when HCP Brain context is provided):",
        "quality_test_satisfied (bool), credibility_drivers_demonstrated (string[]),",
        "trust_breakers_triggered (string[]), likely_objections_addressed (bool),",
        "recommended_rep_approach_used (bool), alignment_rationale (string)",
        "",
        "hcp_brain_coaching must include (when HCP Brain context is provided):",
        "quality_test_feedback, credibility_driver_feedback, trust_breaker_feedback,",
        "objection_alignment_feedback, recommended_rep_move_feedback,",
        "improved_response_grounded_in_hcp_brain",
        "",
        "metric_scores must include:",
        "context_awareness, cue_recognition, empathy_acknowledgment, strategic_questioning,",
        "evidence_framing, objection_handling, conversational_control, tone_pace_confidence",
        "Each metric must be: { score_1_to_10, rationale }",
        "",
        "outcome_analysis must include:",
        "commitment_attempted, commitment_type, commitment_strength,",
        "hcp_progression, conversation_advanced, outcome_rationale,",
        "expected_outcome_for_temperature, actual_outcome, outcome_quality,",
        "gap_between_expected_and_actual, commitment_attempt_quality, progression_rationale",
        "",
        "delivery_impact_on_hcp must include:",
        "perceived_by_hcp, likely_hcp_reaction, impact_on_resistance, impact_on_trust, coaching_implication",
        "",
        "delivery_coaching must include:",
        "pacing_feedback, pause_feedback, confidence_feedback, question_delivery_feedback,",
        "perceived_listening_feedback, recommended_delivery_adjustment, example_rephrasing_with_delivery_note",
        "",
        "Return valid JSON only.",
    ].join("\n");
}
